#to get your "npm run remote" command to work do the following:
# 1. go to the google compute engine dashboard for the project
# 2. find the instance you will be remoting into (there should only be one)
# 3. click on the options for ssh and select "view gcloud command"
# 4. copy this commmand
# 5. in the terminal go to the root of "the-count" project
# 6. create a remote.sh file
# 7. paste the gcloud command from step 4 into this file
# 8. run chmod u+x on remote.sh and bob's your uncle
